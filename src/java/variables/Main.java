public class Main {
  public static void main(String[] args) {
    byte i = 1;
    short s = 2;
    long l = 4L;
    double d = 4.4d;
    String name = "java";
    int year = 2025;
    float version = 17.1f;
    char tag = 'J';
    boolean isInstalled = true;
    final int num = 5;
    int x, y, z;
    x = y = z = 3;

    System.out.println("String -> " + name);
    System.out.println("int -> " + year);
    System.out.println("float -> " + version);
    System.out.println("char -> " + tag);
    System.out.println("boolean -> " + isInstalled);
    System.out.println("final(const) -> " + num);
  }
}
